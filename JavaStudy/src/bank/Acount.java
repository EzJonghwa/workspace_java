package bank;

public class Acount {

private String acName;
private String acOunt;
private int depposit;


public Acount(String acName, String acOunt, int depposit) {

	this.acName = acName;
	this.acOunt = acOunt;
	this.depposit = depposit;
}


public String getAcName() {
	return acName;
}


public String getAcOunt() {
	return acOunt;
}


public int getDepposit() {
	return depposit;
}


}







